// SCREEN SIZE CONFIG
  static setSize(BuildContext context) {
    final double screenWidth =
        MediaQueryData.fromView(View.of(context)).size.width;
    if (screenWidth < 600) {
      scaleWidth = screenWidth / uiWidth;
      fontSize14 = fontSize14 * scaleWidth;
      fontSize15 = fontSize15 * scaleWidth;
      fontSize16 = fontSize16 * scaleWidth;
    }
  }

  static double screenWidth(BuildContext context, {double dividedBy = 1}) {
    return screenSize(context).width / dividedBy;
  }

  static Size screenSize(BuildContext context) {
    return MediaQuery.of(context).size;
  }

  static double screenHeight(BuildContext context) {
    return screenSize(context).height;
  }

  static double scaffoldBodyHeight(BuildContext context) {
    final fullHeight = screenHeight(context);
    final appBarHeight =
        AppBar().preferredSize.height + MediaQuery.of(context).padding.top;
    final scaffoldBodyHeight = fullHeight - appBarHeight;
    return scaffoldBodyHeight;
  }
